package com.team3.tamagochi.store;

public class CharacterDTO{
	private Long character_num;
	private String character_spc;
	private Long character_hp;
	private Long character_atk;
	private Long character_dod;
	private Long character_price;
	
	public Long getCharacter_num() {
		return character_num;
	}
	public void setCharacter_num(Long character_num) {
		this.character_num = character_num;
	}
	public String getCharacter_spc() {
		return character_spc;
	}
	public void setCharacter_spc(String character_spc) {
		this.character_spc = character_spc;
	}
	public Long getCharacter_hp() {
		return character_hp;
	}
	public void setCharacter_hp(Long character_hp) {
		this.character_hp = character_hp;
	}
	public Long getCharacter_atk() {
		return character_atk;
	}
	public void setCharacter_atk(Long character_atk) {
		this.character_atk = character_atk;
	}
	public Long getCharacter_dod() {
		return character_dod;
	}
	public void setCharacter_dod(Long character_dod) {
		this.character_dod = character_dod;
	}
	public Long getCharacter_price() {
		return character_price;
	}
	public void setCharacter_price(Long character_price) {
		this.character_price = character_price;
	}
	
	

}
