package com.team3.tamagochi.store;

public class WeaponDTO {
	
	private Long weapon_num;
	private String weapon_name;
	private String weapon_description;
	private Long weapon_hp;
	private Long weapon_atk;
	private Long weapon_dod;
	private Long weapon_price;
	
	public Long getWeapon_num() {
		return weapon_num;
	}
	public void setWeapon_num(Long weapon_num) {
		this.weapon_num = weapon_num;
	}
	public String getWeapon_name() {
		return weapon_name;
	}
	public void setWeapon_name(String weapon_name) {
		this.weapon_name = weapon_name;
	}
	public String getWeapon_description() {
		return weapon_description;
	}
	public void setWeapon_description(String weapon_description) {
		this.weapon_description = weapon_description;
	}
	public Long getWeapon_hp() {
		return weapon_hp;
	}
	public void setWeapon_hp(Long weapon_hp) {
		this.weapon_hp = weapon_hp;
	}
	public Long getWeapon_atk() {
		return weapon_atk;
	}
	public void setWeapon_atk(Long weapon_atk) {
		this.weapon_atk = weapon_atk;
	}
	public Long getWeapon_dod() {
		return weapon_dod;
	}
	public void setWeapon_dod(Long weapon_dod) {
		this.weapon_dod = weapon_dod;
	}
	public Long getWeapon_price() {
		return weapon_price;
	}
	public void setWeapon_price(Long weapon_price) {
		this.weapon_price = weapon_price;
	}
	
}
