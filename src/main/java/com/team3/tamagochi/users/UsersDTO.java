package com.team3.tamagochi.users;

public class UsersDTO {
	
	private String user_id;
	private String user_pw;
	private String user_name;
	private String user_nickname;
	private String user_phone;
	private String user_email;
	private Integer user_auth;
	private Integer user_resign;
	
	
	
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public String getUser_pw() {
		return user_pw;
	}
	public void setUser_pw(String user_pw) {
		this.user_pw = user_pw;
	}
	public String getUser_name() {
		return user_name;
	}
	public void setUser_name(String user_name) {
		this.user_name = user_name;
	}
	public String getUser_nickname() {
		return user_nickname;
	}
	public void setUser_nickname(String user_nickname) {
		this.user_nickname = user_nickname;
	}
	public String getUser_phone() {
		return user_phone;
	}
	public void setUser_phone(String user_phone) {
		this.user_phone = user_phone;
	}
	public String getUser_email() {
		return user_email;
	}
	public void setUser_email(String user_email) {
		this.user_email = user_email;
	}
	public Integer getUser_auth() {
		return user_auth;
	}
	public void setUser_auth(Integer user_auth) {
		this.user_auth = user_auth;
	}
	public Integer getUser_resign() {
		return user_resign;
	}
	public void setUser_resign(Integer user_resign) {
		this.user_resign = user_resign;
	}
	
	
	

}
